            <p align="justify">Эти цели призваны помочь мировому сообществу достичь существенных, поддающихся измерению
                результатов
                для улучшения условий жизни людей к 2015 году.</p>
            <p align="justify">Давая обязательство работать над достижением ЦРТ, страны также взяли на себя
                ответственность за
                мониторинг результатов и подготовку отчетов по ЦРТ.</p>
            <p align="justify">В целях мониторинга хода реализации этих целей и задач учреждения системы Организации
                Объединенных
                Наций, включая Всемирный банк и Международный валютный фонд, а также Комитет содействия развитию
                Организации
                экономического сотрудничества и развития, под эгидой Канцелярии Генерального секретаря, совместно
                согласовали
                перечень количественных показателей.</p>
            <p align="justify">В данном разделе представлена информация как по международным количественным показателям,
                характеризующим достижение Целей развития тысячелетия, так и по национальным показателям ЦРТ,
                адаптированных для
                Казахстана.</p>
            <p align="justify">Источниками информации для вышеуказанных показателей в Казахстане являются данные
                официальной
